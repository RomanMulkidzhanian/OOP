#include <iostream>
#include <vector>
#include <algorithm>

int main() {
    std::vector<int> data(1000); // Створюємо вектор з 1000 елементів

    // Заповнюємо вектор випадковими значеннями
    for (int i = 0; i < 1000; ++i) {
        data[i] = rand() % 100;
    }

    // Сортуємо вектор (один з можливих варіантів оптимізації)
    std::sort(data.begin(), data.begin() + 200); // Сортуємо лише перші 20%

    // Виконуємо обробку даних, враховуючи вже відсортовані елементи

    return 0;
}
//Принцип Парето (правило 80/20) стверджує, що 80% наслідків виникають від 20% причин. 
//У програмування це означає, що більшість витрат часу або зусиль будуть спрямовані на вирішення 20% проблем, які мають найбільший вплив.