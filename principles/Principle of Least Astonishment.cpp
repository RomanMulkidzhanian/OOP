class Calculator {
public:
    int add(int a, int b) const {
        return a + b;
    }
    int subtract(int a, int b) const {
        return a - b;
    }
    // Не додавайте метод add() для об'єднання рядків, оскільки це може здивувати користувачів
};
//Принцип найменшого здивування говорить, що програма повинна працювати таким чином,
// щоб вона була найбільш передбачуваною для користувачів та розробників. Іншими словами, поведінка системи повинна відповідати очікуванням.