class MathUtilities {
public:
    static int add(int a, int b) {
        return a + b;
    }

    static int subtract(int a, int b) {
        return a - b;
    }
};
//Принцип повторного використання означає, що код повинен бути написаний таким чином,
// щоб його можна було використовувати повторно в різних частинах програми або в інших проектах.
// Це підвищує ефективність розробки та підтримку коду.